void OLED_ShowChar(unsigned char x,unsigned char y,unsigned char chr)
{
    unsigned char c=0, i=0;
    uint8_t ShowChar[16];
    for(i=0; i<13; i=i+2)
		{
    	ShowChar[i] = 0xC0;
    }
/*
    ShowChar[0]=0xC0;
    ShowChar[2]=0xC0;
    ShowChar[4]=0xC0;
    ShowChar[6]=0xC0;
    ShowChar[8]=0xC0;
    ShowChar[10]=0xC0;
    ShowChar[12]=0xC0;
*/
    ShowChar[14] = 0x40;

    c = chr-' ';
    if(x>128-1)   
		{
			x = 0;
			y = y+2;
		}

    /* font , Line 1st  */
    OLED_Set_Pos(x, y);
    for(i=0; i<8; i++)
		{
      ShowChar[(2*i)+1] = F8X16[c*16+(i)];
    }
		I2C_Write_polling(I2C0, OLED_I2C_ADDR, ShowChar, 16);
		
    /* font , Line 2nd  */
    OLED_Set_Pos(x, y+1);
    for(i=0; i<8; i++)
		{
      ShowChar[(2*i)+1] = F8X16[c*16+(i)+8];
    }
		I2C_Write_polling(I2C0, OLED_I2C_ADDR, ShowChar, 16);
}

/* String length , Max = 16 */
void OLED_ShowStr(unsigned char x, unsigned char y, unsigned char *chr)
{
    unsigned char j;
    j = 0;
  
    while (chr[j] != '\0')
    {	
        if(x>120)
            break;
        
        OLED_ShowChar(x, y, chr[j]);
        x += 8;
        j++;
    }
}
